class Severity(str, Enum):
    LOW      = "LOW"
    MEDIUM   = "MEDIUM"
    HIGH     = "HIGH"
    CRITICAL = "CRITICAL"


class StrengthLevel(str, Enum):
    VERY_WEAK   = "VERY_WEAK"
    WEAK        = "WEAK"
    FAIR        = "FAIR"
    GOOD        = "GOOD"
    STRONG      = "STRONG"
    VERY_STRONG = "VERY_STRONG"


# =============================================================================
# MODELS
# =============================================================================

@dataclass(frozen=True, slots=True)
class ValidationIssue:
    """A single structured validation finding."""
    code:     str
    message:  str
    severity: Severity


@dataclass(frozen=True, slots=True)
class PasswordPolicy:
    """
    Immutable enterprise password policy.
    All fields have safe production defaults.
    """
    min_length:                int  = 12
    max_length:                int  = 128
    require_uppercase:         bool = True
    require_lowercase:         bool = True
    require_numbers:           bool = True
    require_special:           bool = True
    allow_unicode:             bool = True
    allow_spaces:              bool = False
    check_common_passwords:    bool = True
    check_keyboard_patterns:   bool = True
    check_sequential_patterns: bool = True
    check_repeated_patterns:   bool = True
    check_year_patterns:       bool = True   # Fix F — new policy flag
    max_consecutive_repeats:   int  = 2
    minimum_strength_score:    int  = 60
    special_characters:        str  = "!@#$%^&*()-_=+[]{};:,.<>?/|"

    def __post_init__(self) -> None:
        if self.min_length < 1:
            raise ValueError("min_length must be >= 1")
        if self.max_length < self.min_length:
            raise ValueError("max_length must be >= min_length")
        if not (0 <= self.minimum_strength_score <= 100):
            raise ValueError("minimum_strength_score must be between 0 and 100")
        if self.max_consecutive_repeats < 1:
            raise ValueError("max_consecutive_repeats must be >= 1")


@dataclass(frozen=True, slots=True)
class PasswordProfile:
    """Cached per-password character analysis. Built once, reused across all validators."""
    password:          str
    length:            int
    has_uppercase:     bool
    has_lowercase:     bool
    has_digit:         bool
    has_special:       bool
    has_unicode:       bool
    has_whitespace:    bool
    unique_characters: int
    charset_size:      int
    entropy_bits:      float


@dataclass(frozen=True, slots=True)
class PasswordAnalysis:
    """Final immutable report returned from validate()."""
    valid:        bool
    score:        int
    strength:     StrengthLevel
    entropy_bits: float
    issues:       Tuple[ValidationIssue, ...]
    profile:      PasswordProfile


